package com.ticket.demo_ticketking.po;


public class TbLv {

  private long lvId;
  private long priceId;
  private String ticketLv;
  private long price;


  public long getLvId() {
    return lvId;
  }

  public void setLvId(long lvId) {
    this.lvId = lvId;
  }


  public long getPriceId() {
    return priceId;
  }

  public void setPriceId(long priceId) {
    this.priceId = priceId;
  }


  public String getTicketLv() {
    return ticketLv;
  }

  public void setTicketLv(String ticketLv) {
    this.ticketLv = ticketLv;
  }


  public long getPrice() {
    return price;
  }

  public void setPrice(long price) {
    this.price = price;
  }

}
