package com.ticket.demo_ticketking.po;


public class TbShow {

  private long showId;
  private String showTime;
  private String showChoice;


  public long getShowId() {
    return showId;
  }

  public void setShowId(long showId) {
    this.showId = showId;
  }


  public String getShowTime() {
    return showTime;
  }

  public void setShowTime(String showTime) {
    this.showTime = showTime;
  }


  public String getShowChoice() {
    return showChoice;
  }

  public void setShowChoice(String showChoice) {
    this.showChoice = showChoice;
  }

}
